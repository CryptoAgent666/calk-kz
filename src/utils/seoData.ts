// SEO-данные для категорий
export const getCategorySEO = (categoryId: string): SEOData => {
  const category = calculatorCategories.find(cat => cat.id === categoryId);

  if (!category) {
    return getHomeSEO();
  }

  const currentLang = i18n.language || 'ru';
  const isKazakh = currentLang === 'kk';
  const url = getLocalizedUrl(`${BASE_URL}/category/${categoryId}`, currentLang);
  const categoryTitle = getLocalizedText(`${categoryId}.title`, 'categories', category.title, currentLang);
  const categoryDescription = getLocalizedText(`${categoryId}.description`, 'categories', category.description, currentLang);
  const calculatorTitles = category.calculators.map(calc =>
    getLocalizedText(`${calc.id}.title`, 'calculators', calc.title, currentLang)
  );
  const calculatorsList = calculatorTitles.join(', ');

  return {
    title: isKazakh
      ? `${categoryTitle} — ${BASE_SITE_NAME}`
      : `${categoryTitle} - ${BASE_SITE_NAME} | Онлайн калькуляторы для Казахстана`,
    description: isKazakh
      ? `${categoryDescription} Құрамына кіреді: ${calculatorsList}. Барлық есептеулер ҚР 2026 жылғы заңнамасына сәйкес.`
      : `${categoryDescription} Включает: ${calculatorsList}. Все расчеты адаптированы под законодательство Республики Казахстан.`,
    keywords: isKazakh
      ? `${categoryTitle.toLowerCase()}, ${calculatorTitles.map(title => title.toLowerCase()).join(', ')}, қазақстан, онлайн калькулятор`
      : `${categoryTitle.toLowerCase()}, ${calculatorTitles.map(title => title.toLowerCase()).join(', ')}, казахстан, онлайн калькулятор`,
    image: DEFAULT_OG_IMAGE,
    url,
    structuredData: generateStructuredData({
      type: 'category',
      categoryId,
      language: currentLang,
      url
    })
  };
};

