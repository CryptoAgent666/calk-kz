interface SEOData {
  title: string;
  description: string;
  keywords?: string;
  image?: string;
  url?: string;
  structuredData?: string;
  robots?: string;
}

// Базовые SEO-данные для сайта
const BASE_SITE_NAME = 'Calk.kz';
const BASE_SITE_DESCRIPTION = 'Лучшие онлайн калькуляторы для Казахстана';
const BASE_URL = 'https://calk.kz';
const DEFAULT_OG_IMAGE = `${BASE_URL}/og-image.png`;

const getLocalizedText = (key: string, ns: string, fallback: string, lang: string) =>
  i18n.t(key, { ns, lng: lang, defaultValue: fallback });

const ensureTrailingSlash = (url: string) => {
  const [base, query] = url.split('?');
  const normalizedBase = base.endsWith('/') ? base : `${base}/`;
  return query ? `${normalizedBase}?${query}` : normalizedBase;
};

const getLocalizedUrl = (url: string, lang: string) => {
  const normalizedUrl = ensureTrailingSlash(url === BASE_URL ? `${BASE_URL}/` : url);
  return localizeUrl(normalizedUrl, lang, BASE_URL);
};

