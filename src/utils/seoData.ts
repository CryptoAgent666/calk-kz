    'home.description',
    'seo',
    'Удобные онлайн-калькуляторы для жителей Казахстана: налоги, кредиты, зарплаты, коммунальные услуги, социальные выплаты. Все расчеты по актуальному законодательству РК 2026 года.',
    currentLang
